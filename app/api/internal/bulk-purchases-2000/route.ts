export async function GET(request: NextRequest) {
  const { searchParams } = request.nextUrl
  const token = searchParams.get("token")
  const mode = searchParams.get("mode") || "check"
  // バッチ処理: offset(開始インデックス), limit(処理件数, デフォルト100)
  const offset = parseInt(searchParams.get("offset") || "0", 10)
  const limit  = parseInt(searchParams.get("limit")  || "100", 10)

  if (token !== TOKEN) {
    return NextResponse.json({ error: "Unauthorized" }, { status: 401 })
  }

  const batch = PURCHASE_DATA.slice(offset, offset + limit)
  const hasMore = offset + limit < PURCHASE_DATA.length
  const nextOffset = offset + limit

  // ---- CHECK モード ----
  if (mode === "check") {
    const results = []
    let noMember = 0, alreadyExists = 0, ready = 0

    for (const d of batch) {
      const mlm = await prisma.mlmMember.findUnique({
        where: { memberCode: d.memberCode },
        include: {
          purchases: {
            where: { productCode: PRODUCT_CODE, purchaseMonth: d.month }
          }
        }
      })
      if (!mlm) {
        noMember++
        results.push({ ...d, status: "NO_MLM_MEMBER" })
      } else if (mlm.purchases.length > 0) {
        alreadyExists++
        results.push({ ...d, status: "ALREADY_EXISTS", existingId: mlm.purchases[0].id.toString() })
      } else {
        ready++
        results.push({ ...d, status: "READY" })
      }
    }

    return NextResponse.json({
      mode: "check",
      pagination: { total: PURCHASE_DATA.length, offset, limit, batchSize: batch.length, hasMore, nextOffset },
      summary: { ready, alreadyExists, noMember },
      results,
    })
  }

  // ---- CREATE モード ----
  if (mode === "create") {
    const created = []
    const skipped = []
    const errors  = []

    for (const d of batch) {
      try {
        const mlm = await prisma.mlmMember.findUnique({
          where: { memberCode: d.memberCode },
          include: {
            purchases: {
              where: { productCode: PRODUCT_CODE, purchaseMonth: d.month }
            }
          }
        })
        if (!mlm) {
          errors.push({ memberCode: d.memberCode, name: d.name, month: d.month, reason: "MlmMemberが存在しない" })
          continue
        }
        if (mlm.purchases.length > 0) {
          skipped.push({ memberCode: d.memberCode, name: d.name, month: d.month, reason: "既に購入履歴あり" })
          continue
        }
        const purchasedAt = new Date(`${d.month}-01T00:00:00.000Z`)
        const totalPoints = d.quantity * POINTS
        const purchase = await prisma.mlmPurchase.create({
          data: {
            mlmMemberId: mlm.id,
            productCode: PRODUCT_CODE,
            productName: PRODUCT_NAME,
            quantity: d.quantity,
            unitPrice: UNIT_PRICE,
            points: POINTS,
            totalPoints,
            purchaseStatus: "one_time",
            purchaseMonth: d.month,
            purchasedAt,
          }
        })
        created.push({
          memberCode: d.memberCode,
          name: d.name,
          month: d.month,
          quantity: d.quantity,
          purchaseId: purchase.id.toString(),
        })
      } catch (err) {
        errors.push({ memberCode: d.memberCode, name: d.name, month: d.month, reason: String(err) })
      }
    }

    return NextResponse.json({
      mode: "create",
      pagination: { total: PURCHASE_DATA.length, offset, limit, batchSize: batch.length, hasMore, nextOffset },
      summary: { created: created.length, skipped: skipped.length, errors: errors.length },
      created, skipped, errors,
    })
  }

  return NextResponse.json({ error: "mode must be check/create" }, { status: 400 })
}
